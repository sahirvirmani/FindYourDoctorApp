    private boolean ValidatePassword(char[] Password, char[] PasswordR) {
        if(Password.length != PasswordR.length)
            return false;
        else
            for(int i = 0; i < Password.length; i++) {
                if(Password[i] != PasswordR[i])
                    return false;
            }
        return true;
    }
